@router.get("/questions/{resource_id}")
def get_quiz_questions(
    resource_id: int,
    user_id: Optional[UUID] = None,
    db: Session = Depends(get_db),
    current_user_id: str = Depends(get_current_user_id),
):
    """
    Return 2 personalised quiz questions for the given resource.

    Questions are generated FRESH per user every call, personalised using:
      • The user's current level  (beginner / intermediate / advanced)
      • Their known misconceptions (concept_tag + how many times wrong)
      • Topics they have already studied (from past quiz attempt history)

    Generated questions are persisted so submit can look them up by ID.
    """
    resource = db.query(LearningResource).filter(LearningResource.id == resource_id).first()
    if not resource:
        raise HTTPException(status_code=404, detail="Resource not found")

    # ── Collect user context for personalisation ─────────────────────────────
    user_level     = "beginner"
    misconceptions = []    # [{"concept_tag": str, "count": int, "last_seen": str}]
    studied_topics = []    # distinct topic strings from past quiz attempts

    if user_id:
        user = db.query(User).filter(User.id == user_id).first()
        if user:
            user_level = (user.level or "beginner").lower()

        # Misconceptions: most frequent first (top 5)
        misc_rows = (
            db.query(UserMisconception)
            .filter(UserMisconception.user_id == user_id)
            .order_by(UserMisconception.count.desc())
            .limit(5)
            .all()
        )
        misconceptions = [
            {
                "concept_tag": m.concept_tag,
                "count":       m.count,
                "last_seen":   m.last_seen.strftime("%Y-%m-%d") if m.last_seen else "",
            }
            for m in misc_rows
        ]

        # Topics already studied = topics of resources the user attempted before
        past_ids = (
            db.query(UserResourceQuizAttempt.learning_resource_id)
            .filter(UserResourceQuizAttempt.user_id == user_id)
            .distinct()
            .all()
        )
        if past_ids:
            topic_rows = (
                db.query(LearningResource.topic)
                .filter(LearningResource.id.in_([r[0] for r in past_ids]))
                .distinct()
                .all()
            )
            studied_topics = [t[0] for t in topic_rows if t[0]]

    # ── Generate personalised questions via OpenAI ───────────────────────────
    raw = _llm_generate_questions(resource, user_level, misconceptions, studied_topics)

    new_qs = []
    for r in raw:
        q = ResourceQuizQuestion(
            learning_resource_id=resource_id,
            question_text=r["question_text"],
            option_a=r["option_a"],
            option_b=r["option_b"],
            option_c=r["option_c"],
            option_d=r["option_d"],
            correct_option=r["correct_option"],
            concept_tag=r.get("concept_tag"),
        )
        db.add(q)
        new_qs.append(q)

    db.commit()
    for q in new_qs:
        db.refresh(q)

    return {
        "resource_id": resource_id,
        "user_level":  user_level,
        "questions": [
            {
                "id": q.id,
                "question_text": q.question_text,
                "options": {
                    "a": q.option_a,
                    "b": q.option_b,
                    "c": q.option_c,
                    "d": q.option_d,
                },
                "concept_tag": q.concept_tag,
            }
            for q in new_qs
        ],
    }


@router.post("/submit")
def submit_quiz(
    body: SubmitQuizRequest,
    db: Session = Depends(get_db),
    current_user_id: str = Depends(get_current_user_id),
):
    """
    Record quiz answers, update misconceptions + level, mark resource viewed.
    Returns score + new level + which answers were right/wrong + LLM explanations.
    """
    user = db.query(User).filter(User.id == body.user_id).first()
    if not user:
        raise HTTPException(status_code=404, detail="User not found")

    resource = db.query(LearningResource).filter(LearningResource.id == body.resource_id).first()
    if not resource:
        raise HTTPException(status_code=404, detail="Resource not found")

    results = []
    score = 0

    for ans in body.answers:
        question = db.query(ResourceQuizQuestion).filter(
            ResourceQuizQuestion.id == ans.question_id,
            ResourceQuizQuestion.learning_resource_id == body.resource_id,
        ).first()

        if not question:
            continue

        chosen = (ans.chosen_option or "").lower().strip()
        is_correct = chosen == question.correct_option.lower()

        option_map = {
            "a": question.option_a,
            "b": question.option_b,
            "c": question.option_c,
            "d": question.option_d,
        }

        if is_correct:
            score += 1
            explanation = None
        else:
            if question.concept_tag:
                _record_misconception(db, user.id, question.concept_tag)
            explanation = _ai_explain_wrong_answer(
                question_text=question.question_text,
                chosen_option=chosen,
                chosen_text=option_map.get(chosen, ""),
                correct_option=question.correct_option,
                correct_text=option_map.get(question.correct_option.lower(), ""),
                concept_tag=question.concept_tag,
            )

        db.add(UserResourceQuizAttempt(
            user_id=user.id,
            learning_resource_id=body.resource_id,
            question_id=question.id,
            chosen_option=chosen,
            is_correct=is_correct,
            answered_at=datetime.utcnow(),
        ))

        results.append({
            "question_id": question.id,
            "chosen": chosen,
            "correct": question.correct_option,
            "is_correct": is_correct,
            "explanation": explanation,
        })

    # Mark resource as viewed (if not already)
    already = db.query(UserLearningResource).filter(
        UserLearningResource.user_id == user.id,
        UserLearningResource.learning_resource_id == body.resource_id,
    ).first()
    if not already:
        db.add(UserLearningResource(
            user_id=user.id,
            learning_resource_id=body.resource_id,
        ))

    user.last_active_at = datetime.now(tz=timezone.utc)
    db.commit()

    new_level = _update_level(db, user)

    misconceptions = (
        db.query(UserMisconception)
        .filter(UserMisconception.user_id == user.id)
        .order_by(UserMisconception.count.desc())
        .limit(5)
        .all()
    )

    return {
        "score": score,
        "total": len(results),
        "percentage": round((score / len(results)) * 100) if results else 0,
        "results": results,
        "new_level": new_level,
        "level_changed": new_level != (user.level or "beginner"),
        "top_misconceptions": [
            {"concept_tag": m.concept_tag, "count": m.count}
            for m in misconceptions
        ],
    }


@router.get("/history/{user_id}/{resource_id}")
def get_quiz_history(
    user_id: UUID,
    resource_id: int,
    db: Session = Depends(get_db),
    current_user_id: str = Depends(get_current_user_id),
):
    """Returns whether the user has already attempted the quiz for a resource."""
    attempts = (
        db.query(UserResourceQuizAttempt)
        .filter(
            UserResourceQuizAttempt.user_id == user_id,
            UserResourceQuizAttempt.learning_resource_id == resource_id,
        )
        .all()
    )
    return {
        "attempted": len(attempts) > 0,
        "score": sum(1 for a in attempts if a.is_correct),
        "total": len(attempts),
    }