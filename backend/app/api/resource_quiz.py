"""
Resource Quiz API
─────────────────
After a user finishes viewing a learning resource they are shown 2 MCQ
questions generated by OpenAI GPT that are PERSONALISED per user:

  Question generation uses:
    1. Resource title, topic, subtopic, description
    2. User's current level  (beginner / intermediate / advanced)
    3. User's known misconceptions  (concept_tag + how many times seen)
    4. Topics the user has already studied  (from quiz attempt history)

  The questions are NOT cached — every user gets a fresh set tailored
  to their own misconceptions so we can track whether those misconceptions
  improve over time.

Submitting answers:
  • Records each answer in user_resource_quiz_attempts
  • Updates UserMisconception for wrong answers (count++)
  • For correct answers on a previously-wrong concept, count is NOT
    decremented here — improvement is inferred by the caller comparing
    counts over time.
  • Generates an LLM explanation for each wrong answer
  • Marks the resource as viewed (upserts UserLearningResource)
  • Re-evaluates the user's level based on rolling quiz performance

Level update logic
──────────────────
We look at the user's last 20 resource-quiz answers.
  ≥ 80 % correct  → promote  (beginner → intermediate → advanced)
  ≤ 40 % correct  → demote   (advanced → intermediate → beginner)
  otherwise        → keep current level
"""
