def _record_misconception(db: Session, user_id: UUID, concept_tag: str) -> None:
    existing = db.query(UserMisconception).filter(
        UserMisconception.user_id == user_id,
        UserMisconception.concept_tag == concept_tag,
    ).first()
    if existing:
        existing.count += 1
        existing.last_seen = datetime.utcnow()
    else:
        db.add(UserMisconception(
            user_id=user_id,
            concept_tag=concept_tag,
            count=1,
            first_seen=datetime.utcnow(),
            last_seen=datetime.utcnow(),
        ))


def _update_level(db: Session, user: User) -> str:
    recent = (
        db.query(UserResourceQuizAttempt.is_correct)
        .filter(UserResourceQuizAttempt.user_id == user.id)
        .order_by(UserResourceQuizAttempt.answered_at.desc())
        .limit(ROLLING_WINDOW)
        .all()
    )
    if not recent:
        return user.level or "beginner"

    correct = sum(1 for (r,) in recent if r)
    ratio = correct / len(recent)

    current = (user.level or "beginner").lower()
    idx = LEVEL_ORDER.index(current) if current in LEVEL_ORDER else 0

    if ratio >= PROMOTE_THRESHOLD and idx < len(LEVEL_ORDER) - 1:
        new_level = LEVEL_ORDER[idx + 1]
    elif ratio <= DEMOTE_THRESHOLD and idx > 0:
        new_level = LEVEL_ORDER[idx - 1]
    else:
        new_level = current

    if new_level != current:
        user.level = new_level
        db.commit()

    return new_level


