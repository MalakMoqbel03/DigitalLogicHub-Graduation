def _call_openai(prompt: str, max_tokens: int = 800) -> str:
    """
    Raw call to OpenAI chat completions endpoint.
    Reads OPENAI_API_KEY from environment.  Raises on any error.
    """
    api_key = os.environ.get("OPENAI_API_KEY", "")
    if not api_key:
        raise ValueError("OPENAI_API_KEY is not set in environment")

    response = httpx.post(
        "https://api.openai.com/v1/chat/completions",
        headers={
            "Authorization": f"Bearer {api_key}",
            "Content-Type": "application/json",
        },
        json={
            "model": "gpt-4o-mini",          # fast + cheap; swap to gpt-4o if needed
            "max_tokens": max_tokens,
            "temperature": 0.4,              # low temp = consistent, focused output
            "messages": [
                {
                    "role": "system",
                    "content": (
                        "You are an expert digital logic professor. "
                        "You create precise, pedagogically sound multiple-choice questions. "
                        "You always respond with valid JSON only — no markdown, no prose."
                    ),
                },
                {"role": "user", "content": prompt},
            ],
        },
        timeout=25.0,
    )
    response.raise_for_status()
    data = response.json()
    return data["choices"][0]["message"]["content"].strip()


def _build_question_prompt(
    resource: "LearningResource",
    user_level: str,
    misconceptions: list[dict],   # [{"concept_tag": str, "count": int, "last_seen": str}, ...]
    studied_topics: list[str],    # topics the user already completed quizzes on
) -> str:
    """
    Build the personalised question-generation prompt from all available
    user context.  This is the core of the personalisation system.
    """
    topic    = (resource.topic    or "digital logic").replace("_", " ")
    subtopic = (resource.subtopic or "").replace("_", " ")
    title    = resource.title or topic
    desc     = (resource.description or "")[:600] or "No description provided."

    # ── Level guidance ──────────────────────────────────────────────────────
    level_guidance = {
        "beginner": (
            "The student is a BEGINNER. Use simple vocabulary. "
            "Questions should test basic conceptual understanding — "
            "definitions, simple truth tables, direct application. "
            "Avoid tricky edge cases."
        ),
        "intermediate": (
            "The student is at INTERMEDIATE level. "
            "Questions should require applying concepts to small examples, "
            "comparing alternatives, or identifying correct circuit behaviour."
        ),
        "advanced": (
            "The student is ADVANCED. "
            "Questions should require deep reasoning — multi-step problems, "
            "subtle distinctions, optimisation trade-offs, or edge cases."
        ),
    }.get(user_level.lower(), "The student's level is unknown; aim for intermediate difficulty.")

    # ── Misconception section ───────────────────────────────────────────────
    if misconceptions:
        misc_lines = []
        for m in misconceptions[:4]:          # top 4 most frequent
            tag   = m["concept_tag"].replace("_", " ")
            count = m["count"]
            misc_lines.append(f'  • "{tag}" — wrong {count} time(s)')
        misc_block = (
            "KNOWN MISCONCEPTIONS (most important — at least one question MUST "
            "directly probe whether this misconception persists or has been corrected):\n"
            + "\n".join(misc_lines)
            + "\n\nFor each misconception question: make the wrong answer reflect exactly "
            "how a student with THAT misconception would reason, so a correct answer "
            "proves the misconception is resolved."
        )
    else:
        misc_block = (
            "No known misconceptions yet. Generate two well-rounded questions "
            "that cover the most important concepts of this resource."
        )

    # ── Prior study context ─────────────────────────────────────────────────
    if studied_topics:
        prior = ", ".join(t.replace("_", " ") for t in studied_topics[:6])
        prior_block = f"Topics already studied by this student: {prior}."
    else:
        prior_block = "This is one of the student's first resources."

    # ── Final prompt ────────────────────────────────────────────────────────
    return f"""You are creating a personalised 2-question quiz for a digital logic student.

=== LEARNING RESOURCE ===
Title:       {title}
Topic:       {topic}{(' / ' + subtopic) if subtopic else ''}
Description: {desc}

=== STUDENT PROFILE ===
Level:  {user_level.capitalize()}
Instruction: {level_guidance}
{prior_block}

=== MISCONCEPTION TARGETING ===
{misc_block}

=== YOUR TASK ===
Generate exactly 2 multiple-choice questions.
Rules:
- Each question has exactly 4 options (a, b, c, d) with ONE correct answer.
- Wrong options must be plausible — common mistakes, not obviously silly.
- The concept_tag field must be a short snake_case label for the concept tested
  (e.g. "boolean_algebra", "karnaugh_map", "flip_flop_timing").
- Questions must be directly answerable from the resource described above.
- Difficulty must match the student level described.

Respond ONLY with a valid JSON array — no markdown fences, no explanation:
[
  {{
    "question_text": "...",
    "option_a": "...",
    "option_b": "...",
    "option_c": "...",
    "option_d": "...",
    "correct_option": "a",
    "concept_tag": "snake_case_concept"
  }},
  {{
    "question_text": "...",
    "option_a": "...",
    "option_b": "...",
    "option_c": "...",
    "option_d": "...",
    "correct_option": "b",
    "concept_tag": "snake_case_concept"
  }}
]"""


def _llm_generate_questions(
    resource: "LearningResource",
    user_level: str,
    misconceptions: list[dict],
    studied_topics: list[str],
) -> list[dict]:
    """
    Call OpenAI to generate 2 personalised MCQ questions.
    Falls back to static questions on any failure.

    Args:
        resource:        The LearningResource ORM object.
        user_level:      'beginner' | 'intermediate' | 'advanced'
        misconceptions:  List of dicts from UserMisconception rows.
        studied_topics:  List of topic strings the user already attempted.
    """
    prompt = _build_question_prompt(resource, user_level, misconceptions, studied_topics)
    try:
        raw = _call_openai(prompt, max_tokens=1000)

        # Strip accidental markdown fences
        raw = raw.strip()
        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]
        raw = raw.strip()

        questions = json.loads(raw)

        required = {
            "question_text", "option_a", "option_b",
            "option_c", "option_d", "correct_option",
        }
        for q in questions:
            assert required.issubset(q.keys()), f"Missing keys in question: {q.keys()}"
            assert q["correct_option"] in ("a", "b", "c", "d"), \
                f"Invalid correct_option: {q['correct_option']}"

        print(f"[quiz] Generated {len(questions)} personalised questions via OpenAI "
              f"for resource {resource.id}, user_level={user_level}, "
              f"misconceptions={[m['concept_tag'] for m in misconceptions[:4]]}")
        return questions[:2]

    except Exception as e:
        print(f"[quiz] OpenAI question generation failed: {e!r} — using static fallback")
        return _static_fallback_questions(resource)


def _static_fallback_questions(resource: "LearningResource") -> list[dict]:
    """Hardcoded fallback when OpenAI is unavailable."""
    topic = (resource.topic or "this topic").replace("_", " ")
    return [
        {
            "question_text": f"Which of the following best describes a key concept in {topic}?",
            "option_a": "It simplifies complex logic into basic operations",
            "option_b": "It always increases system complexity",
            "option_c": "It is only applicable to analog circuits",
            "option_d": "It removes the need for binary representation",
            "correct_option": "a",
            "concept_tag": resource.topic or "general",
        },
        {
            "question_text": f"What is the primary purpose of studying {topic}?",
            "option_a": "To memorise all possible truth tables",
            "option_b": "To design and analyse digital logic systems",
            "option_c": "To replace software with hardware unconditionally",
            "option_d": "To avoid using Boolean algebra",
            "correct_option": "b",
            "concept_tag": resource.topic or "general",
        },
    ]


def _ai_explain_wrong_answer(
    question_text: str,
    chosen_option: str,
    chosen_text: str,
    correct_option: str,
    correct_text: str,
    concept_tag: str | None,
) -> str:
    """
    Ask OpenAI to produce a short, encouraging explanation of why the
    student's answer was wrong.  Falls back to a plain string.
    """
    topic_label = concept_tag.replace("_", " ") if concept_tag else "digital logic"
    prompt = (
        f"A student answered a digital logic quiz question incorrectly.\n\n"
        f"Question: {question_text}\n"
        f"Student chose ({chosen_option.upper()}): {chosen_text}\n"
        f"Correct answer ({correct_option.upper()}): {correct_text}\n"
        f"Topic: {topic_label}\n\n"
        "In 2-3 concise sentences, explain encouragingly WHY the student's choice "
        "is incorrect and what makes the correct answer right. "
        "Keep language simple and supportive. Do not use bullet points."
    )
    try:
        return _call_openai(prompt, max_tokens=200)
    except Exception:
        return (
            f"The correct answer is {correct_option.upper()}: \"{correct_text}\". "
            f"Review the material on {topic_label} to strengthen this concept."
        )


