router = APIRouter()

# ── level ordering ─────────────────────────────────────────────────────────────
LEVEL_ORDER = ["beginner", "intermediate", "advanced"]

PROMOTE_THRESHOLD = 0.80
DEMOTE_THRESHOLD  = 0.40
ROLLING_WINDOW    = 20


